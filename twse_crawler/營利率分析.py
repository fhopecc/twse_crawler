from zhongwen.程式 import 通知執行時間

@通知執行時間
def 分析營利率(股票) -> "pandas.Series":
    """
    一、營利率與毛利率係產品定價權及護城河強弱之雙核心指標。兩者雙殺代表結構性惡化（崩塌）；
        若毛利持穩而營利下滑，則屬短暫費用內耗，兩者交叉能精準辨識護城河純度。
    二、特別修正：聯立「營收同比」，若營收動能連續失速且利潤率低於常態，
        即可在第一時間揪出護城河正遭市場「無情填平」的昔日巨頭（如智基）。
    三、報告項目：最新營利率、長期營利率中位數、營利率底線、營利率是否近四季連續下滑、
                  最新毛利率、長期毛利率中位數、護城河近期是否結構性崩塌、
                  護城河是否遭市場填平、
                  營利率近期是否嚴重惡化
    """
    import numpy as np
    import pandas as pd
    from twse_crawler.財報分析 import 取財報彙總表

    資料表 = 取財報彙總表(股票)
    
    # ==========================================
    # 1. 基礎數據計算 (雙利率交叉聯立)
    # ==========================================
    資料表["營業毛利率"] = np.where(
        資料表["營收"] > 0, 資料表["毛利"] / 資料表["營收"], 0
    )
    資料表["營利率"] = np.where(
        資料表["營收"] > 0, 資料表["營利"] / 資料表["營收"], 0
    )

    # ==========================================
    # 2. 宏觀分析：10 年（40季）歷史基準線 (Baseline)
    # ==========================================
    # 滾動取過去 40 季的營利率中位數與標準差，界定常態臨界防線
    長期營利率中位數 = 資料表["營利率"].tail(40).median()
    長期營利率標準差 = 資料表["營利率"].tail(40).std()
    營利率底線 = 長期營利率中位數 - (1.5 * 長期營利率標準差)

    # 同步計算長線毛利率基準線
    長期毛利率中位數 = 資料表["營業毛利率"].tail(40).median()

    # ==========================================
    # 3. 微觀分析：短線滾動趨勢 (近 4 季)
    # ==========================================
    # 檢查營利率短線變動
    資料表["營利率當季是否下滑"] = 資料表["營利率"].diff() < 0
    營利率是否近四季連續下滑 = 資料表["營利率當季是否下滑"].rolling(window=4).sum() == 4
    營利率是否近四季連續下滑 = 營利率是否近四季連續下滑.iloc[-1]

    # 檢查毛利率短線變動
    資料表["毛利率當季是否下滑"] = 資料表["營業毛利率"].diff() < 0

    # ==========================================
    # 4. 領先指標：營收長線動能檢查 (營收同比)
    # ==========================================
    # 計算最新一季營收同比 (相較於去年同季)
    去年同季營收 = 資料表["營收"].shift(4)
    資料表["營收同比"] = np.where(
        去年同季營收 > 0, 
        (資料表["營收"] - 去年同季營收) / 去年同季營收, 
        0
    )
    
    # 核心過濾：營收同比連續兩季衰退超過 10% (市場需求或市佔率發生結構性萎縮)
    資料表["營收年減超標"] = 資料表["營收同比"] < -0.10
    營收動能連續嚴重失速 = 資料表["營收年減超標"].rolling(window=2).sum() == 2
    營收動能連續嚴重失速 = 營收動能連續嚴重失速.iloc[-1]

    # ==========================================
    # 5. 提取最新一季數據與結構性過濾
    # ==========================================
    最新一季數據 = 資料表.iloc[-1]
    最新營利率 = 最新一季數據["營利率"]
    最新毛利率 = 最新一季數據["營業毛利率"]

    # 🚨 雷達 A：護城河近期是否結構性崩塌
    # 聯立條件：最新營利率穿透歷史臨界底線，且最新毛利率比上一季下滑（轉嫁能力斷裂）
    近期是否跌破臨界底線 = 最新營利率 < 營利率底線
    護城河近期是否結構性崩塌 = 近期是否跌破臨界底線 and 最新一季數據["毛利率當季是否下滑"]

    # ⚠️ 雷達 B：護城河是否遭市場填平 (專殺智基類型的核心補丁)
    # 聯立條件：營收動能連續兩季年減超標（營收同比連續失速），且最新營利率跌破長線中位數
    護城河是否遭市場填平 = 營收動能連續嚴重失速 and (最新營利率 < 長期營利率中位數)

    # ==========================================
    # 6. 終極熔斷判定 (三軌聯立大閘)
    # ==========================================
    營利率近期是否嚴重惡化 = (
        護城河近期是否結構性崩塌 or 
        護城河是否遭市場填平 or 
        營利率是否近四季連續下滑 or 
        (最新營利率 < 0)
    )

    # ==========================================
    # 打包成標準 Series 輸出
    # ==========================================
    評估結果 = pd.Series({
        "最新營利率": round(最新營利率, 3),
        "長期營利率中位數": round(長期營利率中位數, 3),
        "營利率底線": round(營利率底線, 3),
        "營利率是否近四季連續下滑": 營利率是否近四季連續下滑,
        "最新毛利率": round(最新毛利率, 3),
        "長期毛利率中位數": round(長期毛利率中位數, 3),
        "護城河近期是否結構性崩塌": 護城河近期是否結構性崩塌,
        "護城河是否遭市場填平": 護城河是否遭市場填平,
        "營利率近期是否嚴重惡化": 營利率近期是否嚴重惡化
    })
    評估結果.name = 股票 
    return 評估結果
