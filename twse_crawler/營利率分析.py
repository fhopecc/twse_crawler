from zhongwen.程式 import 通知執行時間

@通知執行時間
def 分析營利率(股票) -> "pandas.Series":
    """
    一、營利率與毛利率係產品定價權及護城河強弱之雙核心指標。
        最新一季毛利率與營利率若發生結構性「雙殺」，代表產品定價權斷裂（如台積電短線大逆風），
        此指標將直接觸發「護城河近期是否結構性崩塌」之硬熔斷。
    二、報告項目：最新營利率、長期營利率中位數、營利率底線、營利率是否近四季連續下滑、
                  最新毛利率、長期毛利率中位數、毛利率及營利率雙殺、
                  營利率近期是否嚴重惡化
    """
    import numpy as np
    import pandas as pd
    from twse_crawler.財報分析 import 取財報彙總表

    資料表 = 取財報彙總表(股票)

    # ==========================================
    # 1. 基礎數據計算 (雙利率交叉聯立)
    # ==========================================
    資料表["毛利率"] = np.where(資料表["營收"] > 0, 資料表["毛利"] / 資料表["營收"], 0)
    資料表["營利率"] = np.where(資料表["營收"] > 0, 資料表["營利"] / 資料表["營收"], 0)

    # ==========================================
    # 2. 長期營利率係 10 年（40季）歷史基準線 (Baseline)
    # ==========================================
    長期營利率中位數 = 資料表["營利率"].tail(40).median()
    長期營利率標準差 = 資料表["營利率"].tail(40).std()
    # 營利率底線為長期營利率中位數低 1.5 倍標準差
    營利率底線 = 長期營利率中位數 - (1.5 * 長期營利率標準差)
    長期毛利率中位數 = 資料表["毛利率"].tail(40).median()

    # ==========================================
    # 3. 微觀分析：短線滾動趨勢 (近 4 季)
    # ==========================================
    # 檢查營利率短線變動
    資料表["營利率當季是否下滑"] = 資料表["營利率"].diff() < 0
    營利率是否近四季連續下滑 = 資料表["營利率當季是否下滑"].rolling(window=4).sum() == 4
    營利率是否近四季連續下滑 = 營利率是否近四季連續下滑.iloc[-1]

    # 檢查毛利率短線變動
    資料表["毛利率當季是否下滑"] = 資料表["毛利率"].diff() < 0

    # ==========================================
    # 4. 提取最新一季數據
    # ==========================================
    最新一季數據 = 資料表.iloc[-1]
    最新營利率 = 最新一季數據["營利率"]
    最新毛利率 = 最新一季數據["毛利率"]

    # 🚨 雷達：毛利率及營利率雙殺 (毛利營利雙殺大閘)
    # 聯立條件：最新營利率穿透歷史臨界底線，且最新毛利率比上一季下滑（定價權碎裂、轉嫁能力斷裂）
    近期是否跌破臨界底線 = 最新營利率 < 營利率底線
    毛利率及營利率雙殺 = 近期是否跌破臨界底線 and 最新一季數據["毛利率當季是否下滑"]

    # ==========================================
    # 5. 終極熔斷判定 (季報雙軌聯立大閘)
    # ==========================================
    營利率近期是否嚴重惡化 = (
        毛利率及營利率雙殺 or 營利率是否近四季連續下滑 or (最新營利率 < 0)
    )

    # ==========================================
    # 打包成標準 Series 輸出 (剔除月營收變數)
    # ==========================================
    評估結果 = pd.Series(
        {
            "最新營利率": round(最新營利率, 3),
            "長期營利率中位數": round(長期營利率中位數, 3),
            "營利率底線": round(營利率底線, 3),
            "營利率是否近四季連續下滑": 營利率是否近四季連續下滑,
            "最新毛利率": round(最新毛利率, 3),
            "長期毛利率中位數": round(長期毛利率中位數, 3),
            "毛利率及營利率雙殺": 毛利率及營利率雙殺,
            "營利率近期是否嚴重惡化": 營利率近期是否嚴重惡化,
        }
    )
    評估結果.name = 股票
    return 評估結果
