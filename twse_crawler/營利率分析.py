from zhongwen.程式 import 通知執行時間

@通知執行時間
def 分析營利率(股票) -> "pandas.Series":
    """
    一、營利率與毛利率係產品定價權及護城河強弱之雙核心指標。兩者雙殺代表結構性惡化；
        若毛利持穩而營利下滑，則屬短暫費用內耗，兩者交叉能精準辨識護城河純度。
    二、報告項目：最新營利率、長期營利率中位數、歷史常態低點門檻、近四季連續下滑、
                  最新毛利率、長期毛利率中位數、護城河結構性崩塌、營利率惡化嚴重
    """
    import numpy as np
    import pandas as pd
    from twse_crawler.財報分析 import 取財報彙總表

    資料表 = 取財報彙總表(股票)
    
    # ==========================================
    # 1. 基礎數據計算 (雙利率交叉聯立)
    # ==========================================
    # 備註：此處假設您的財報彙總表已包含：營收、營業毛利(或成本)、營利 欄位
    # 若彙總表無直接提供毛利，可用 (營收 - 成本) 代替
    資料表["營業毛利率"] = np.where(
        資料表["營收"] > 0, 資料表["毛利"] / 資料表["營收"], 0
    )
    資料表["營利率"] = np.where(
        資料表["營收"] > 0, 資料表["營利"] / 資料表["營收"], 0
    )

    # ==========================================
    # 2. 宏觀分析：10 年（40季）歷史基準線 (Baseline)
    # ==========================================
    # 滾動取過去 40 季的營利率中位數與標準差，界定常態地平線
    長期營利率中位數 = 資料表["營利率"].tail(40).median()
    長期營利率標準差 = 資料表["營利率"].tail(40).std()
    歷史常態低點門檻 = 長期營利率中位數 - (1.5 * 長期營利率標準差)

    # 同步計算長線毛利率基準線
    長期毛利率中位數 = 資料表["毛利率"].tail(40).median()

    # ==========================================
    # 3. 微觀分析：短線滾動趨勢 (近 4 季)
    # ==========================================
    # 檢查營利率短線變動
    資料表["營利率當季是否下滑"] = 資料表["營利率"].diff() < 0
    近四季連續下滑 = 資料表["營利率當季是否下滑"].rolling(window=4).sum() == 4
    近四季連續下滑 = 近四季連續下滑.iloc[-1]

    # 檢查毛利率短線變動
    資料表["毛利率當季是否下滑"] = 資料表["毛利率"].diff() < 0

    # ==========================================
    # 4. 提取最新一季的審查數據與結構性過濾
    # ==========================================
    最新一季數據 = 資料表.iloc[-1]
    最新營利率 = 最新一季數據["營利率"]
    最新毛利率 = 最新一季數據["毛利率"]

    # 🛡️ 核心補丁：護城河結構性崩塌雷達
    # 聯立條件：最新一季「營利率跌破歷史常態地板」而且「最新一季毛利率也比上一季下滑」
    近期是否跌破常態低點 = 最新營利率 < 歷史常態低點門檻
    護城河結構性崩塌 = 近期是否跌破常態低點 and 最新一季數據["毛利率當季是否下滑"]

    # ==========================================
    # 5. 終極熔斷判定
    # ==========================================
    # 升級後的硬核雙軌熔斷：
    # 軌道 A：發生最慘烈的「護城河結構性崩塌」（毛利、營利雙殺，跌破常態地板）。
    # 軌道 B：近期營利率連續 4 季無抵抗連續下滑。
    # 軌道 C：絕對防線 —— 營利率直接轉負（本業發生虧損）。
    # (備註：若僅是近期跌破常態低點，但毛利率其實在上升，此處不會盲目觸發嚴重惡化，避免誤殺)
    營利率惡化嚴重 = 護城河結構性崩塌 or 近四季連續下滑 or (最新營利率 < 0)

    # ==========================================
    # 打包成標準字典輸出，供主控制台進行最終決策
    # ==========================================
    評估結果 = pd.Series({
        "最新營利率": round(最新營利率, 3),
        "長期營利率中位數": round(長期營利率中位數, 3),
        "歷史常態低點門檻": round(歷史常態低點門檻, 3),
        "近四季連續下滑": 近四季連續下滑,
        "最新毛利率": round(最新毛利率, 3),
        "長期毛利率中位數": round(長期毛利率中位數, 3),
        "護城河結構性崩塌": 護城河結構性崩塌,
        "營利率惡化嚴重": 營利率惡化嚴重
    })
    
    return 評估結果
