@通知執行時間
def 分析負債結構(股票) -> "pandas.Series":
    """
    一、有息負債係報酬率純度之過濾指標，財務槓桿過高或長期無法維持淨現金體質，
        將使資本結構脆弱，致未來系統性風險增加。
    二、報告項目：最新有息負債占比、長期有息負債占比中位數、有息負債占比天花板、
                  最新淨負債權益比、長期淨負債中位數、
                  有息負債占比近期是否凸破天花板、
                  有息負債占比近期是否大幅增加、財務結構近期是否嚴重惡化
    三、有息負債占比天花板係長期有息負債占比中位數加 1.5 倍標準差。
    """
    import numpy as np
    import pandas as pd
    from zhongwen.表 import 表示
    from twse_crawler.財報分析 import 取財報彙總表

    資料表 = 取財報彙總表(股票)
    資料表["股東權益"]  = 資料表.權益總額
    資料表["有息負債"] = (
        資料表["短期借款"].fillna(0) +
        資料表["應付短期票券"].fillna(0) +
        資料表["一年或一營業週期內到期長期負債"].fillna(0) +
        資料表["長期借款"].fillna(0) +
        資料表["應付公司債"].fillna(0) 
    )
    m = []
    # ==========================================
    # 1. 基礎數據計算 (計算金融有息負債占比與純純的淨負債權益比)
    # ==========================================
    資料表["有息負債占比"] = np.where(
        資料表["資產總計"] > 0, 資料表["有息負債"] / 資料表["資產總計"], 0
    )
    # 淨負債權益比公式：(有息負債 - 現金及約當現金) / 股東權益
    # 若算出來為負數，代表現金大於借款，屬於高安全性的「淨現金公司」
    資料表["淨負債權益比"] = np.where(
        資料表["股東權益"] > 0, 
        (資料表["有息負債"] - 資料表["現金及約當現金"]) / 資料表["股東權益"], 
        0
    )

    # ==========================================
    # 2. 宏觀分析：10 年（40季）歷史基準線 (Baseline)
    # ==========================================
    # 滾動取過去 40 季的中位數與標準差，界定該公司長線的資本結構血統
    長期有息負債占比中位數 = 資料表["有息負債占比"].tail(40).median()
    長期有息負債標準差 = 資料表["有息負債占比"].tail(40).std()
    長期淨負債中位數 = 資料表["淨負債權益比"].tail(40).median()

    # 以有息負債占比的中位數加上 1.5 倍標準差，定義為該公司「歷史槓桿常態天花板」
    有息負債占比天花板 = 長期有息負債占比中位數 + (1.5 * 長期有息負債標準差)

    # ==========================================
    # 3. 微觀分析：短線滾動趨勢 (最新一季相較於自身的常態變動)
    # ==========================================
    # 檢查最新一季的有息負債占比是否衝破歷史常態天花板
    最新一季數據 = 資料表.iloc[-1]
    最新有息負債占比 = 最新一季數據["有息負債占比"]
    最新淨負債權益比 = 最新一季數據["淨負債權益比"]

    有息負債占比近期是否凸破天花板 = 最新有息負債占比 > 有息負債占比天花板

    # 檢查有息負債占比短線是否大舉增加（例如最新一季比上一季增加超過 5 個百分點）
    資料表["有息負債占比變動"] = 資料表["有息負債占比"].diff()
    最近有息負債占比變動 = 資料表["有息負債占比變動"].iloc[-1]
    有息負債占比近期是否大幅增加 = 最近有息負債占比變動 > 0.05
    # ==========================================
    # 4. 終極熔斷判定
    # ==========================================
    # 雙軌槓桿危機熔斷邏輯：
    # 軌道 A：最新有息負債占比衝破歷史常態門檻，且短線大舉舉債（大幅增加）。
    # 軌道 B：硬核硬指標防線 —— 當最新有息負債占比直接大於 35%，或最新淨負債權益比飆破 50% 臨界值。
    財務結構近期是否嚴重惡化 = (
        (有息負債占比近期是否凸破天花板 and 有息負債占比近期是否大幅增加) or
        (最新有息負債占比 > 0.35) or
        (最新淨負債權益比 > 0.50)
    )
    if 有息負債占比近期是否凸破天花板 and 有息負債占比近期是否大幅增加:
        m.append(
            f'有息負債占比{最新有息負債占比:,.0%}'
            f'已凸破歷史天花板{有息負債占比天花板:,.0%}'
            f'且較上期大幅增加{最近有息負債占比變動:,.0%}'
            )

    if 最新有息負債占比 > 0.35:
        m.append(f'有息負債占比高達{最新有息負債占比:,.0%}')

    if 最新淨負債權益比 > 0.5:
        m.append(f'淨負債權益比高達{最新淨負債權益比:,.0%}')

    評估結果 = pd.Series({
        "最新有息負債占比": 最新有息負債占比,
        "長期有息負債占比中位數": 長期有息負債占比中位數,
        "有息負債占比天花板": 有息負債占比天花板,
        "最新淨負債權益比": 最新淨負債權益比,
        "長期淨負債中位數": 長期淨負債中位數,
        "有息負債占比近期是否凸破天花板": 有息負債占比近期是否凸破天花板,
        "有息負債占比近期是否大幅增加": 有息負債占比近期是否大幅增加,
        "財務結構近期是否嚴重惡化": 財務結構近期是否嚴重惡化
    })
    評估結果.name = 股票
    評估結果['評語']= '；'.join(m)
    return 評估結果

def 評定負債結構分數(股票) -> "pandas.Series":
    '''
    一、第二部分：負債結構扣分 10 分。
    '''
    r = 分析負債結構(股票)
    m = r.評語.split('；')
    最新有息負債占比 = r["最新有息負債占比"]
    最新淨負債權益比 = r["最新淨負債權益比"]
    長期淨負債中位數 = r["長期淨負債中位數"]
    負債近期嚴重惡化 = r["財務結構近期是否嚴重惡化"]

    負債扣分項 = 0

    if 負債近期嚴重惡化:
        負債扣分項 = 10
    else:
        if 最新淨負債權益比 > 0 and 最新淨負債權益比 > 長期淨負債中位數:
            負債扣分項 += 3

        if 最新有息負債占比 > 0.20:
            負債扣分項 += 2

    負債扣分項 = max(0, min(10, 負債扣分項))

    if 負債扣分項 > 0:
        m = [f"【負債結構】扣除 {負債扣分項:,.0f} 分"] + m
    r['分數'] = 負債扣分項
    r['評語'] = '；'.join(m)
    return r
