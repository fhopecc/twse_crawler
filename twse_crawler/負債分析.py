def 分析有息負債(股票) -> "pandas.Series":
    """
    一、有息負債係報酬率純度之過濾指標，財務槓桿過高或長期無法維持淨現金體質，
        將使資本結構脆弱，致未來系統性風險增加。
    二、報告項目：最新有息負債比、長期有息負債中位數、最新淨負債比、長期淨負債中位數、
                  有息負債比大幅增加、財務結構惡化嚴重
    """
    import numpy as np
    import pandas as pd
    from twse_crawler.財報分析 import 取財報彙總表

    資料表 = 取財報彙總表(股票)
    資料表["股東權益"]  = 資料表.權益總額
    # 備註：此處假設您的財報彙總表已包含：短期借款、長期借款、總資產、現金及約當現金、股東權益 欄位
    # 若彙總表無 '有息負債' 總額，則現場加總
    資料表["有息負債"] = (
        資料表["短期借款"] + 
        資料表["應付短期票券"] + 
        資料表["一年或一營業週期內到期長期負債"] + 
        資料表["應付公司債"] + 
        資料表["長期借款"]
    )

    # ==========================================
    # 1. 基礎數據計算 (計算金融有息負債比與純純的淨負債比)
    # ==========================================
    資料表["有息負債比"] = np.where(
        資料表["總資產"] > 0, 資料表["有息負債"] / 資料表["總資產"], 0
    )
    
    # 淨負債比公式：(有息負債 - 現金及約當現金) / 股東權益
    # 若算出來為負數，代表現金大於借款，屬於高安全性的「淨現金公司」
    資料表["淨負債比"] = np.where(
        資料表["股東權益"] > 0, 
        (資料表["有息負債"] - 資料表["現金及約當現金"]) / 資料表["股東權益"], 
        0
    )

    # ==========================================
    # 2. 宏觀分析：10 年（40季）歷史基準線 (Baseline)
    # ==========================================
    # 滾動取過去 40 季的中位數與標準差，界定該公司長線的資本結構血統
    長期有息負債中位數 = 資料表["有息負債比"].tail(40).median()
    長期有息負債標準差 = 資料表["有息負債比"].tail(40).std()
    
    長期淨負債中位數 = 資料表["淨負債比"].tail(40).median()

    # 以有息負債比的中位數加上 1.5 倍標準差，定義為該公司「歷史槓桿常態天花板」
    歷史槓桿惡化門檻 = 長期有息負債中位數 + (1.5 * 長期有息負債標準差)

    # ==========================================
    # 3. 微觀分析：短線滾動趨勢 (最新一季相較於自身的常態變動)
    # ==========================================
    # 檢查最新一季的有息負債比是否衝破歷史常態天花板
    最新一季數據 = 資料表.iloc[-1]
    最新有息負債比 = 最新一季數據["有息負債比"]
    最新淨負債比 = 最新一季數據["淨負債比"]

    近期是否逾歷史槓桿門檻 = 最新有息負債比 > 歷史槓桿惡化門檻

    # 檢查有息負債比短線是否大舉增加（例如最新一季比上一季增加超過 5 個百分點）
    資料表["有息負債比變動"] = 資料表["有息負債比"].diff()
    有息負債比大幅增加 = 資料表["有息負債比變動"].iloc[-1] > 0.05

    # ==========================================
    # 4. 終極熔斷判定
    # ==========================================
    # 雙軌槓桿危機熔斷邏輯：
    # 軌道 A：最新有息負債比衝破歷史常態門檻，且短線大舉舉債（大幅增加）。
    # 軌道 B：硬核硬指標防線 —— 當最新有息負債比直接大於 35%，或最新淨負債比飆破 50% 臨界值。
    財務結構惡化嚴重 = (
        (近期是否逾歷史槓桿門檻 and 有息負債比大幅增加) or 
        (最新有息負債比 > 0.35) or 
        (最新淨負債比 > 0.50)
    )

    # ==========================================
    # 打包成標準字典輸出，供主控台進行最終基礎分扣分或熔斷判定
    # ==========================================
    評估結果 = pd.Series({
        "最新有息負債比": round(最新有息負債比, 3),
        "長期有息負債中位數": round(長期有息負債中位數, 3),
        "歷史槓桿惡化門檻": round(歷史槓桿惡化門檻, 3),
        "最新淨負債比": round(最新淨負債比, 3),
        "長期淨負債中位數": round(長期淨負債中位數, 3),
        "近期是否逾歷史槓桿門檻": 近期是否逾歷史槓桿門檻,
        "有息負債比大幅增加": 有息負債比大幅增加,
        "財務結構惡化嚴重": 財務結構惡化嚴重
    })
    評估結果.name = '分析結果' 
    return 評估結果
