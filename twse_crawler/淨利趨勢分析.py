def 分析同比差異主因(歷季數據表, 股票, 項目, 子項):
    '''
    一、歷季數據表係傳回指定股票歷季數據之函數。
    二、示例：稅前淨利=營利+業外損益，則稅前淨利係主項，由營利及業外損益2個子項組成。
    三、結果：同比差異主因、差異值。
    四、分析失敗則回傳空序列。
    五、同比差異主因：係同比差異最大之子項，即影響項目同比差異最大之子項。
    '''
    from zhongwen.表 import 數據不足, 顯示
    import pandas as pd
    try:
        if not isinstance(歷季數據表, pd.DataFrame):
            歷季數據表 = 歷季數據表(股票)
        使用項目 = [項目] + 子項
        同比差異 = 歷季數據表[使用項目].iloc[-1] - 歷季數據表[使用項目].iloc[-5]
        子項同比差異 = 同比差異[子項]
        同比差異主因 = 子項同比差異.abs().idxmax()
        差異值 = 子項同比差異[同比差異主因]
        r = pd.Series()
        r['同比差異主因'] = 同比差異主因
        r['差異值'] = 差異值
        return r
    except IndexError as e:
        raise 數據不足(f'{股票}之營利及業外損益', len(歷季數據表), 5, '分析同比差異主因')
    except KeyError as e:
        if 歷季數據表[子項].isna().all().all():
            raise 數據不足(f'{股票}之營利及業外損益', 0, 1, '分析同比差異主因')
        raise e

def 分析科目增減情形(歷史數據, 數據欄位, 時間欄位, 同比欄位=None, 環比欄位=None
                    ,原因欄位=None, 數據名稱=None, 期間="月"
                    ,減少為利多=False, 傳回分析數據=False):
    '''分析科目增減幅度、連續增減次數、由增加轉為減少、由營利轉為損失、未依損益轉換本期同比連續增減次數；指定啟用傳回分析數據，則會傳回字典包括數據明細，否則為評分字典'''
    from zhongwen.數 import 計算增減率
    from zhongwen.文 import 刪除末尾句號
    from zhongwen.時 import 取民國月份
    from zhongwen.表 import 顯示
    import pandas as pd
    import numpy as np
    if not 數據名稱:
        數據名稱 = 數據欄位
    歷史數據.sort_values(時間欄位, inplace=True)
    # 顯示(歷史數據, 顯示筆數=1000)
    本期數據 = 歷史數據.iloc[-1]
    if pd.isna(本期數據[數據欄位]):
        return pd.Series()
    最早數據 = 歷史數據.iloc[0]

    本期 = 本期數據[時間欄位]
    本期民國期間 = 取民國月份(本期) 
    最早民國期間 = 取民國月份(最早數據[時間欄位]) 
    df = 歷史數據[數據欄位].fillna(0)
    本期數值 = df.iloc[-1]

    if 同比欄位:
        同比 = 本期數據[同比欄位]
    else:
        if 期間 == '月':
            去年同期 = df.shift(12)  # 去年同期
        elif 期間 == '季':
            去年同期 = df.shift(4)  # 去年同期
        else:
            raise Exception(f'不支援的期間[{期間}]')

        歷史同比 = pd.Series([
            計算增減率(去年同期數, 本期數)
            for 去年同期數, 本期數 in zip(去年同期, df)
        ])
        除錯 = False
        if 除錯 and '稅前淨利' in 數據名稱:
            歷史數據['本期'] = df
            歷史數據['去年同期'] = 去年同期
            歷史數據['同比'] = 歷史同比
            # 顯示(歷史數據)
            breakpoint()
        同比 = 歷史同比.iloc[-1]

    if 環比欄位:
        環比 = 本期數據[環比欄位]
    else:
        上期 = df.shift(1)  # 上期
        環比 = (df - 上期) / 上期
        歷史環比 = pd.Series([
            計算增減率(上期數, 本期數)
            for 上期數, 本期數 in zip(上期, df)
        ])
        環比 = 歷史環比.iloc[-1]

    評分 = 0
    評分說明 = ''
    if 同比 == np.inf:
        損益表達 = 數據名稱.replace('損益', '利益')
        評分說明 = f'由去年同期轉為{損益表達}'
        評分 = 20 
    elif 同比 == -np.inf:
        損益表達 = 數據名稱.replace("利", "損").replace('損益', '損失')
        評分說明 = f'由去年同期轉為{損益表達}'
        評分 = -20 
    elif 環比 == np.inf:
        損益表達 = 數據名稱.replace('損益', '利益')
        評分說明 = f'由上{期間}轉為{損益表達}'
        評分 = 20 
    elif 環比 == -np.inf:
        損益表達 = 數據名稱.replace("利", "損").replace('損益', '損失')
        評分說明 = f'由上{期間}轉為{損益表達}'
        評分 = -20 
    elif ~np.isinf(同比) and ~np.isinf(環比) and 同比 > 0.3 and 環比 > 0.3:
        評分說明 = f'年增{同比:,.2%}，{期間}增{環比:,.2%}'
        評分 = 20 
    elif ~np.isinf(同比) and ~np.isinf(環比) and 同比 < -0.3 and 環比 < -0.3:
        評分說明 = f'年減{-同比:,.2%}，{期間}減{-環比:,.2%}'
        評分 = -20 
    elif ~np.isinf(同比) and 同比 > 0.3:
        評分說明 = f'年增{同比:,.2%}'
        評分 = 10 
    elif ~np.isinf(同比) and 同比 < -0.3:
        評分說明 = f'年減{-同比:,.2%}'
        評分 = -10 
    elif ~np.isinf(環比) and 環比 > 0.3:
        評分說明 = f'{期間}增{環比:,.2%}'
        評分 = 10 
    elif ~np.isinf(環比) and 環比 < -0.3:
        評分說明 = f'{期間}減{-環比:,.2%}'
        評分 = 10 
    if 同比欄位:
        歷史同比 = 歷史數據[同比欄位] 

    同比增減 = 歷史同比 > 0
    同比增減組別 = (同比增減 != 同比增減.shift()).cumsum()
    同比連續增減次數 = 同比增減.groupby(同比增減組別).cumcount() + 1
    本期同比連續增減次數 = 未依損益轉換本期同比連續增減次數 = \
        同比連續增減次數.iloc[-1]*同比增減.replace({True:1, False:-1}).iloc[-1]
        
    除錯 = False
    if 除錯 and '稅前淨利' in 數據名稱:
        歷史數據['本期'] = df
        歷史數據['去年同期'] = 去年同期
        歷史數據['同比'] = 歷史同比
        歷史數據['同比增減'] = 同比增減
        歷史數據['同比連續增減次數'] = 同比連續增減次數
        歷史數據['本期同比連續增減次數'] = 本期同比連續增減次數 
        # 顯示(歷史數據)
        breakpoint()

    if ~np.isinf(同比) and 本期同比連續增減次數 == 1:
        if len(評分說明) > 0:
            評分說明 = f'轉為年增，{評分說明}'
        else:
            評分說明 = f'轉為年增'
        評分 += 20 
    elif ~np.isinf(同比) and 本期同比連續增減次數 == -1:
        if len(評分說明) > 0:
            評分說明 = f'轉為年減，{評分說明}'
        else:
            評分說明 = f'轉為年減'
        評分 -= 20 
    elif 1 < 本期同比連續增減次數 < 12:
        if len(評分說明) > 0:
            評分說明 += f'，連增{本期同比連續增減次數}個{期間}'
        else:
            評分說明 = f'連增{本期同比連續增減次數}個{期間}'
        評分 += 10 
    elif -12 < 本期同比連續增減次數 < -1:
        if len(評分說明) > 0:
            評分說明 += f'，連減{abs(本期同比連續增減次數)}個{期間}'
        else:
            評分說明 = f'連減{abs(本期同比連續增減次數)}個{期間}'
        評分 -= 10 
    elif 本期同比連續增減次數 > 12:
        if len(評分說明) > 0:
            評分說明 += f'，連增{本期同比連續增減次數}個{期間}'
        else:
            評分說明 = f'連增{本期同比連續增減次數}個{期間}'
        評分 += 100 
    elif 本期同比連續增減次數 < -12:
        if len(評分說明) > 0:
            評分說明 += f'，連減{abs(本期同比連續增減次數)}個{期間}'
        else:
            評分說明 = f'連減{abs(本期同比連續增減次數)}個{期間}'
        評分 -= 100

    本期歷史增加排名 = df.rank(ascending=False, method="min").iloc[-1]
    本期歷史減少排名 = df.rank(ascending=True, method="min").iloc[-1]

    if 歷史數據.shape[0] > 12: # 排名至少要有12個月
        if 本期歷史增加排名 == 1:
            if len(評分說明) > 0:
                評分說明 += f'，創{最早民國期間}以來{歷史數據.shape[0]}個月新高'
            else:
                評分說明 = f'創{最早民國期間}以來{歷史數據.shape[0]}個月新高'
            評分 += 200
        elif 本期歷史減少排名 == 1:
            if len(評分說明) > 0:
                評分說明 += f'，創{最早民國期間}以來{歷史數據.shape[0]}個月新低'
            else:
                評分說明 += f'創{最早民國期間}以來{歷史數據.shape[0]}個月新低'
            評分 -= 200
        elif 1 < 本期歷史增加排名 <= 3:
            if len(評分說明) > 0:
                評分說明 += f'，為{最早民國期間}以來第{本期歷史增加排名:.0f}高'
            else:
                評分說明 += f'為{最早民國期間}以來第{本期歷史增加排名:.0f}高'
            評分 += 100
        elif 1 < 本期歷史減少排名 <= 3:
            if len(評分說明) > 0:
                評分說明 += f'，為{最早民國期間}以來第{本期歷史減少排名:.0f}低'
            else:
                評分說明 += f'為{最早民國期間}以來第{本期歷史減少排名:.0f}低'
            評分 -= 100

    增減原因 = ''
    if 原因欄位:
        增減原因 = 本期數據[原因欄位]
        增減原因 = 刪除末尾句號(增減原因) \
            if isinstance(增減原因, str) and 增減原因 != '-' else ''
        if len(增減原因) > 0:
            增減原因 = '，主要係' + 增減原因 
            評分 += 100 # 有公開增減原因，資訊較為開放係利多

    if len(評分說明) > 0:
        if 評分說明[:5] == "由去年同期":
            評分說明 = f'{本期民國期間}{評分說明}'
        else:
            評分說明 = f'{本期民國期間}{數據名稱}{評分說明}'
        評分說明 = 評分說明.replace('年減，年減', '年減').replace('年增，年增', '年增')

    if 本期數據[數據欄位] > 0:
        評分說明 = 評分說明.replace("損益", "利益")
    else:
        評分說明 = 評分說明.replace("損益", "損失").replace("利", "損")
        if '增' in 評分說明:
            評分說明 = 評分說明.replace("增", "減").replace("高", "低")
        else:
            評分說明 = 評分說明.replace("減", "增").replace("低", "高")
        本期同比連續增減次數 *= -1

    評分結果 = pd.Series()
    if 減少為利多:
        評分 *= -1
    評分結果[評分說明] = 評分
    if 傳回分析數據:
        評分結果['說明'] = 評分說明
        評分結果['連續增減次數'] = 本期同比連續增減次數 
        評分結果['未依損益轉換本期同比連續增減次數'] = 未依損益轉換本期同比連續增減次數
    return 評分結果

