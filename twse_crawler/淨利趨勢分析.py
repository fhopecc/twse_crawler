def 分析營利(股票):
    '''
    一、結果項目：說明。
    二、營利=營收-成本-費用
    '''
    from twse_crawler.趨勢分析 import 分析本季同比, 分析同比差異主次因
    from twse_crawler.損益表分析 import 取損益表
    from zhongwen.表 import 顯示, 數據不足
    from zhongwen.數 import 取增減百分比
    import pandas as pd
    分析結果 = pd.Series()
    try:
        r = 分析同比差異主次因(取損益表, 股票, '營利', ['營收', '成本', '費用'])
        歷季損益表 = 取損益表(股票)
        營利同比分析結果 = 分析本季同比(歷季損益表, '營利', '財報日期', 表達數據季別=False)
        if r.主因 == '營收':
            營收分析結果 = 分析本季同比(歷季損益表, '營收', '財報日期', 表達數據季別=False)
            if len(營收分析結果.說明) > 0:
                分析結果['說明'] = 營收分析結果.說明 + '，應分析月營收趨勢'
            else:
                同比 = 營收分析結果.同比
                分析結果['說明'] = f'營收同比{取增減百分比(同比, 無小數=True)}，應分析月營收趨勢'
        elif r.主因 == '成本':
            成本分析結果 = 分析本季同比(歷季損益表, '成本', '財報日期', 表達數據季別=False)
            分析結果['說明'] = 成本分析結果.說明

        elif r.主因 == '費用': 
            費用分析結果 = 分析本季同比(歷季損益表, '費用', '財報日期', 表達數據季別=False)
            分析結果['說明'] = 費用分析結果.說明

        if r.次因 == '營收':
            營收分析結果 = 分析本季同比(歷季損益表, '營收', '財報日期', 表達數據季別=False)
            if len(營收分析結果.說明) > 0:
                說明 = '，及' + 營收分析結果.說明 + '，應分析月營收趨勢'
            else:
                同比 = 營收分析結果.同比
                說明 = '，及' + f'營收同比{取增減百分比(同比, 無小數=True)}，應分析月營收趨勢'
            if r.主因 in ['成本', '費用']:
                說明 = 說明.replace(
                        '，及', '，而').replace(
                                '年增', '僅增').replace(
                                        '，應分析', '所致，應分析')
            分析結果['說明'] += 說明
        elif r.次因 == '成本': 
            成本分析結果 = 分析本季同比(歷季損益表.fillna(0), '成本', '財報日期', 表達數據季別=False)
            if r.主因 == "營收":
                if r.主因差異值 * r.次因差異值 > 0:
                    if r.主因差異值 > 0:
                        說明 = '，' + 成本分析結果.說明.replace('年增', '亦隨增')
                    else:
                        說明 = '，雖' + 成本分析結果.說明 + '，仍未能補足營收減少之缺口'
                elif r.主因差異值 * r.次因差異值 < 0:
                    說明 = '，及' + 成本分析結果.說明 
                分析結果['說明'] = 分析結果.說明.replace('，應分析月營收趨勢'
                                                        ,f'{說明}，應分析月營收趨勢')
            else:
                分析結果['說明'] = 成本分析結果.說明

        elif r.次因 == '費用': 
            費用分析結果 = 分析本季同比(歷季損益表, '費用', '財報日期', 表達數據季別=False)
            if r.主因 == "成本":
                if r.主因差異值 * r.次因差異值 > 0:
                    說明 = '，及' + 費用分析結果.說明
                elif r.主因差異值 * r.次因差異值 < 0:
                    說明 = '，雖' + 費用分析結果.說明 + '，仍不足彌補成本增加缺口'
                分析結果['說明'] += 說明
            elif r.主因 == "營收":
                if r.主因差異值 < 0:
                    if 營利同比分析結果.同比 > 0:
                        說明 = 費用分析結果.說明
                        分析結果['說明'] = 分析結果.說明.replace('營收年減'
                                                                ,f'{說明}，而營收僅年減'
                                                                ).replace('，應分析月營收趨勢'
                                                                ,f'，營利率增加所致，應分析月營收趨勢'
                                                                )

                    else:
                        說明 = '，雖' + 費用分析結果.說明 + '，仍不足彌補營收減少缺口所致'
                        分析結果['說明'] = 分析結果.說明.replace('，應分析月營收趨勢'
                                                                ,f'{說明}，應分析月營收趨勢'
                                                                )
            else:
                說明 = 費用分析結果.說明
                分析結果['說明'] += 說明


        if 分析結果['說明'] == '':
            分析結果['說明'] = 營利同比分析結果.說明
        else:
            分析結果['說明'] = 營利同比分析結果.說明 + "，應分析月自結營利，係" + 分析結果.說明
        return 分析結果
    except 數據不足 as e:
        e.目的 = '分析營利'
        raise e

