from zhongwen.程式 import 通知執行時間

@通知執行時間
def 分析資金循環天數(股票) -> "pandas.Series":
    """
    一、資金循環天數係報酬率之原因及領先指標，資金循環天數增加即經營效率惡化，
        將致未來報酬率降低。
    二、報告項目：最新天數、歷史惡化天數門檻、近四季連續惡化、近期是否逾惡化天數門檻、
                  資金循環惡化嚴重
    """
    import numpy as np
    import pandas as pd
    from twse_crawler.財報分析 import 取財報彙總表
    資料表 = 取財報彙總表(股票)
    資料表['應收帳款'] = 資料表.應收帳款淨額 
    資料表["營業成本"] = 資料表.成本 + 資料表.費用

    # 1. 基礎數據計算 (台灣財報為單季，週轉天數以 90 天計算)
    資料表["應收帳款週轉天數"] = np.where(
        資料表["營收"] > 0, (資料表["應收帳款"] / 資料表["營收"]) * 90, 0
    )
    資料表["存貨週轉天數"] = np.where(
        資料表["營業成本"] > 0, (資料表["存貨"] / 資料表["營業成本"]) * 90, 0
    )
    資料表["應付帳款付現天數"] = np.where(
        資料表["營業成本"] > 0, (資料表["應付帳款"] / 資料表["營業成本"]) * 90, 0
    )

    # 核心指標：營運資金循環天數（CCC）
    資料表["資金循環天數"] = (
        資料表["應收帳款週轉天數"]
        + 資料表["存貨週轉天數"]
        - 資料表["應付帳款付現天數"]
    )

    # 2. 宏觀分析：10 年（40季）歷史基準線 (Baseline)
    # 滾動取過去 40 季的中位數與標準差，定義出這家公司自身的「健康常態範圍」
    長期中位數 = 資料表["資金循環天數"].tail(40).median()
    長期標準差 = 資料表["資金循環天數"].tail(40).std()

    # 以中位數加上 1.5 倍標準差，作為該公司歷史上的「極限壓力天花板」
    歷史惡化天數門檻 = 長期中位數 + (1.5 * 長期標準差)

    # 3. 微觀分析：短線滾動趨勢 (近 4 季)
    資料表["當季是否惡化"] = 資料表["資金循環天數"].diff() > 0
    # 檢查是否連續 4 個季度，CCC 天數都比上一季還要拉長
    連續四季惡化 = 資料表["當季是否惡化"].rolling(window=4).sum() == 4

    # 4. 提取最新一季的審查數據
    最新一季數據 = 資料表.iloc[-1]
    最新天數 = 最新一季數據["資金循環天數"]

    # 5. 終極熔斷判定：如果近期連續惡化，且最新天數已經「衝破 10 年歷史天花板」
    近期是否逾惡化天數門檻 = 最新天數 > 歷史惡化天數門檻
    資金循環惡化嚴重 = 連續四季惡化.iloc[-1] and 近期是否逾惡化天數門檻

    # 打包成標準字典輸出，供主控台進行最終權重或乘數綜合打分
    評估結果 = pd.Series({
        "最新天數": round(最新天數, 1),
        "長期中位數": round(長期中位數, 1),
        "歷史惡化天數門檻": round(歷史惡化天數門檻, 1),
        "近四季連續惡化": 連續四季惡化.iloc[-1],
        "近期是否逾惡化天數門檻": 近期是否逾惡化天數門檻,
        "資金循環惡化嚴重": 資金循環惡化嚴重
    })
    return 評估結果
