from zhongwen.程式 import 通知執行時間

@通知執行時間
def 分析資金循環天數(股票) -> "pandas.Series":
    """
    一、資金循環天數係報酬率之原因及領先指標，天數增加即經營效率惡化，
        將致未來報酬率降低。
    二、報告項目：最新循環天數、循環天數長期中位數、可接受循環天數天花板、
                  循環天數近四季是否連續惡化、近期惡化是否逾天花板、
                  最新應付帳款天數是否增加、應付帳款信用惡化、
                  資金循環是否嚴重惡化
    """
    import numpy as np
    import pandas as pd
    from twse_crawler.財報分析 import 取財報彙總表

    資料表 = 取財報彙總表(股票)
    資料表['應收帳款'] = 資料表.應收帳款淨額 
    資料表["營業成本"] = 資料表.成本 + 資料表.費用
    資料表["營業現金流"]  = 資料表['營業活動之淨現金流入（流出）'].fillna(0)

    # ==========================================
    # 1. 基礎數據計算 (台灣財報為單季，週轉天數以 90 天計算)
    # ==========================================
    資料表["應收帳款週轉天數"] = np.where(
        資料表["營收"] > 0, (資料表["應收帳款"] / 資料表["營收"]) * 90, 0
    )
    資料表["存貨週轉天數"] = np.where(
        資料表["營業成本"] > 0, (資料表["存貨"] / 資料表["營業成本"]) * 90, 0
    )
    資料表["應付帳款付現天數"] = np.where(
        資料表["營業成本"] > 0, (資料表["應付帳款"] / 資料表["營業成本"]) * 90, 0
    )

    # 核心指標：營運資金循環天數（CCC）
    資料表["資金循環天數"] = (
        資料表["應收帳款週轉天數"]
        + 資料表["存貨週轉天數"]
        - 資料表["應付帳款付現天數"]
    )

    # ==========================================
    # 2. 宏觀分析：10 年（40季）歷史基準線 (Baseline)
    # ==========================================
    # 滾動取過去 40 季的中位數與標準差，定義出這家公司自身的「健康常態範圍」
    長期中位數 = 資料表["資金循環天數"].tail(40).median()
    長期標準差 = 資料表["資金循環天數"].tail(40).std()

    # 以中位數加上 1.5 倍標準差，作為該公司歷史可接受循環天數天花板 
    可接受循環天數天花板 = 長期中位數 + (1.5 * 長期標準差)

    # ==========================================
    # 3. 微觀分析：短線滾動趨勢 (近 4 季與聯立雷達)
    # ==========================================
    # (1) 資金循環天數滾動檢查
    資料表["當季是否惡化"] = 資料表["資金循環天數"].diff() > 0
    連續四季惡化 = 資料表["當季是否惡化"].rolling(window=4).sum() == 4

    # (2) 🛡️ 應付帳款惡性拖欠雷達聯立檢查
    # 檢查各項指標是否比上一季增加
    資料表["應付帳款天數增加"] = 資料表["應付帳款付現天數"].diff() > 0
    資料表["存貨天數增加"] = 資料表["存貨週轉天數"].diff() > 0
    
    # 聯立條件：1. 應付帳款天數增加 且 2. 營業現金流為負(失血) 且 3. 存貨天數增加(滯銷)
    # (備註：此處假設您的財報彙總表內建有 '營業現金流' 欄位)
    資料表["應付帳款信用惡化"] = (
        資料表["應付帳款天數增加"] & 
        (資料表["營業現金流"] < 0) & 
        資料表["存貨天數增加"]
    )

    # ==========================================
    # 4. 提取最新一季的審查數據
    # ==========================================
    最新一季數據 = 資料表.iloc[-1]
    最新循環天數 = 最新一季數據["資金循環天數"]
    最新應付帳款天數增加 = 最新一季數據["應付帳款天數增加"]
    最新應付帳款信用惡化 = 最新一季數據["應付帳款信用惡化"]

    # ==========================================
    # 5. 終極熔斷判定
    # ==========================================
    近期惡化逾天花板 = 最新循環天數 > 可接受循環天數天花板
    
    # 雙軌熔斷邏輯：
    # 軌道 A：常態資金循環連續四季惡化，且衝破歷史天花板
    # 軌道 B：最新一季觸發應付帳款惡性拖欠（信用惡化）
    資金循環惡化嚴重 = (連續四季惡化.iloc[-1] and 近期惡化逾天花板) or 最新應付帳款信用惡化

    # ==========================================
    # 打包成標準字典輸出，供主控台進行最終綜合打分
    # ==========================================
    評估結果 = pd.Series({
        "最新循環天數": round(最新循環天數, 1),
        "循環天數長期中位數": round(長期中位數, 1),
        "可接受循環天數天花板": round(可接受循環天數天花板, 1),
        "循環天數近四季是否連續惡化": 連續四季惡化.iloc[-1],
        "近期惡化是否逾天花板": 近期惡化逾天花板,
        "最新應付帳款天數是否增加": 最新應付帳款天數增加,
        "最新應付帳款信用是否惡化": 最新應付帳款信用惡化,
        "資金循環是否嚴重惡化": 資金循環惡化嚴重
    })
    評估結果.name = 股票
    return 評估結果
