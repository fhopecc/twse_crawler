from zhongwen.程式 import 通知執行時間

@通知執行時間
def 分析營收動能(股票) -> "pandas.Series":
    """
    一、本函數為最外層「一槍斃命營收動能熔斷器」，採用「無監督特徵分流」機制。
    二、自動分流三軌雷達：
        1. 營建/工程雷達：若最新一季「合約負債/總資產 > 10%」，自動關閉月營收機制，改用「合約負債長線動能」把關。
        2. 期貨金融雷達：若最新一季「期貨交易人權益/總資產 > 50%」，改用「客戶保證金長線動能」把關（如元大期）。
        3. 常態動能雷達：其餘常態企業（如數字科技、智基、常態製造業），開啟月營收快速熔斷雷達。
    三、報告項目：分流產業類別、特徵科目佔比、最新單月營收同比、
                 最新合約負債同比、最新客戶保證金同比、是否觸發動能硬熔斷
    """
    import numpy as np
    import pandas as pd
    from twse_crawler.營收分析 import 取歷月營收表
    from twse_crawler.財報分析 import 取財報彙總表

    # 1. 讀取最新季報進行資產結構特徵識別
    季報表 = 取財報彙總表(股票)
    最新季報 = 季報表.iloc[-1]
    總資產 = 最新季報["資產總計"]
    最新營收同比 = np.nan
    合約負債 = 最新季報["合約負債－流動"] if "合約負債－流動" in 最新季報 else 0
    # 期貨交易人權益即期貨商的客戶保證金會計科目
    客戶保證金 = 最新季報["客戶保證金專戶"] if "客戶保證金專戶" in 最新季報 else 0

    合約負債佔比 = 合約負債 / 總資產 if 總資產 > 0 else 0
    保證金佔比 = 客戶保證金 / 總資產 if 總資產 > 0 else 0

    # 初始化輸出變數
    分流產業類別 = "常態製造與零售業"
    特徵科目佔比 = 0.0
    最新單月營收同比 = 0.0
    最新合約負債同比 = 0.0
    最新客戶保證金同比 = 0.0
    是否觸發動能硬熔斷 = False

    # ==========================================
    # 2. 三軌雷達自動特徵分流判斷
    # ==========================================

    # 軌道 A：營建 / 工程服務型企業
    if 合約負債佔比 > 0.10:
        分流產業類別 = "大蛇吞象"
        特徵科目佔比 = round(合約負債佔比, 3)

        # 計算最新一季合約負債同比
        去年同季合約負債 = 季報表["合約負債－流動"].shift(4)
        季報表["合約負債同比"] = np.where(
            去年同季合約負債 > 0,
            (季報表["合約負債－流動"] - 去年同季合約負債) / 去年同季合約負債,
            0,
        )
        最新合約負債同比 = 季報表["合約負債同比"].iloc[-1]

        # 熔斷標準：水庫乾涸（合約負債按年大減 15% 以上）
        if 最新合約負債同比 < -0.15:
            是否觸發動能硬熔斷 = True

    # 軌道 B：期貨經紀金融業 (專殺元大期)
    elif 保證金佔比 > 0.50:
        分流產業類別 = "食客戶保證金利息"
        特徵科目佔比 = round(保證金佔比, 3)

        # 計算最新一季客戶保證金（期貨交易人權益）同比
        去年同季保證金 = 季報表["客戶保證金專戶"].shift(4)
        季報表["保證金同比"] = np.where(
            去年同季保證金 > 0,
            (季報表["客戶保證金專戶"] - 去年同季保證金) / 去年同季保證金,
            0,
        )
        最新客戶保證金同比 = 季報表["保證金同比"].iloc[-1]

        # 熔斷標準：客戶大退資、資金規模大幅縮水 15% 以上
        if 最新客戶保證金同比 < -0.15:
            是否觸發動能硬熔斷 = True

    # 軌道 C：常態性開門營業公司 (數字科技、智基、台積電、聯發科)
    else:
        分流產業類別 = "天天進帳"
        特徵科目佔比 = 0.0

        # 讀取每個月 10 號前發布的月營收公報數據
        月營收表 = 取歷月營收表(股票)
        if 月營收表.empty:
            raise ValueError(f'{股票}無月營收資料！')
        # 計算最新單月營收同比
        去年同月營收 = 月營收表["營收"].shift(12)
        月營收表["月營收同比"] = np.where(
            去年同月營收 > 0,
            (月營收表["營收"] - 去年同月營收) / 去年同月營收,
            0,
        )

        最新營收同比 = 月營收表["月營收同比"].iloc[-1]

        # 核心防護：月營收年減超標，且連續兩個月同比大跌 15% 以上
        月營收表["月營收失速"] = 月營收表["月營收同比"] < -0.15
        營收動能連續嚴重失速 = 月營收表["月營收失速"].rolling(window=2).sum() == 2
        營收動能連續嚴重失速 = 營收動能連續嚴重失速.iloc[-1]

        if 營收動能連續嚴重失速:
            是否觸發動能硬熔斷 = True

    # ==========================================
    # 打包 Series 輸出
    # ==========================================
    動能評估結果 = pd.Series(
        {
            "分流產業類別": 分流產業類別,
            "特徵科目佔比": 特徵科目佔比,
            "最新營收同比": 最新營收同比,
            "最新合約負債同比": 最新合約負債同比,
            "最新客戶保證金同比": 最新客戶保證金同比,
            "是否觸發動能硬熔斷": 是否觸發動能硬熔斷,
        }
    )
    動能評估結果.name = 股票
    return 動能評估結果
