def 分析崑鼎型公司(股票) -> "pandas.Series":
    '''
    一、財務特徵：
        1.無形資產占比達15％，主營項目係投資興建及改建公共設施取得之特許經營權，
          前開投資成本列入服務特許權協議，故服務特許權協議為資產主要部分，
          又因 XBRL 僅列其上級科目無形資產，
          故設定無形資產占比達15％。
        2.其他非流動負債占比達3％，前開協議含屆滿須確保設施可用交還政府之義務，
          爰起始以折現法估計復原費用列入應計復原費用，
          並同額列入服務特許權協議以分期攤銷至營業成本，
          故應計復原費用應為主要負債之一，
          又因 XBRL 僅列其上級科目其他非流動負債，
          故設定其他非流動負債占比達3％。
        3.攤銷費用與服務特許權協議比例之變異係數小於0.05，即近似一常數，
          主要係反映以直線法攤銷所致，
          惟 XBRL 僅列無形資產淨額，即原始服務特許權協議減除累積攤銷，
          而無法計算攤銷費用與服務特許權協議比例。
    二、營收即將下滑訊號：
        1.其他非流動負債環比減少逾1成，係應計復原費用在合約期滿前轉至應付費用。
        2.特許經營權乾涸警訊：無形資產連2季減少且較八季前減少逾1成。
    '''
    from twse_crawler.財報分析 import 取財報彙總表
    from twse_crawler.營收分析 import 取歷月營收表
    from twse_crawler.現流表分析 import 取現流表
    import pandas as pd
    import numpy as np

    h = 取財報彙總表(股票) # h 為歷史季報
    h['其他非流動負債環比'] = h.其他非流動負債.pct_change(1)
    h['無形資產環比'] = h.無形資產.pct_change(1)
    h['無形資產八季同比'] = h.無形資產.pct_change(8)

    h['合約即將到期警訊'] = (h.其他非流動負債環比 <= -0.10)

    h['無形資產乾涸警訊'] = (
        (h.無形資產環比 < 0) & 
        (h.無形資產環比.shift(1) < 0) & 
        (h.無形資產八季同比 <= -0.10)
    )
    h['營收即將下滑訊號'] = h.合約即將到期警訊 | h.無形資產乾涸警訊

    r = h.iloc[-1] # r 為最新季報
    無形資產占比 = r.無形資產 / r.資產總計
    其他非流動負債占比 = r.其他非流動負債 / r.資產總計

    return pd.Series(
            {"無形資產占比":無形資產占比
            ,"其他非流動負債占比":其他非流動負債占比
            ,"是否為崑鼎型公司":無形資產占比 > 0.15 and 其他非流動負債占比 > 0.02
            ,"無形資產環比":r.無形資產環比
            ,"無形資產八季同比":r.無形資產八季同比
            ,"合約即將到期警訊":r.合約即將到期警訊
            ,"特許經營權是否乾涸":r.無形資產乾涸警訊
            ,"營收即將下滑訊號":r.營收即將下滑訊號
            })

def 分析營收模式(股票) -> tuple:
    """【特徵工程與分流模組】

    傳入「股票」代號，於函數內部抓取財報及月營收數據，鑑定公司的營收模式。

    回傳:
        tuple: (營收模式, 特徵科目佔比)
    """
    from twse_crawler.營收分析 import 取歷月營收表
    from twse_crawler.財報分析 import 取財報彙總表

    # 1. 讀取最新季報資料
    季報表 = 取財報彙總表(股票)
    if 季報表.empty:
        raise ValueError(f"{股票}無財報彙總資料！")

    最新季報 = 季報表.iloc[-1]

    總資產 = 最新季報["資產總計"]
    合約負債 = 最新季報["合約負債－流動"] if "合約負債－流動" in 最新季報 else 0
    客戶保證金 = 最新季報["客戶保證金專戶"] if "客戶保證金專戶" in 最新季報 else 0

    合約負債佔比 = 合約負債 / 總資產 if 總資產 > 0 else 0
    保證金佔比 = 客戶保證金 / 總資產 if 總資產 > 0 else 0

    # 2. 軌道 A 與 軌道 D：高合約負債與蓄水池型企業
    if 合約負債佔比 > 0.01:  # 只要合約負債大於基本水位 (1%) 進入特徵分流
        # 讀取近 12 個月營收計算波動度 (CV)
        月營收表 = 取歷月營收表(股票)
        if not 月營收表.empty and len(月營收表) >= 12:
            近一年月營收 = 月營收表["營收"].iloc[-12:]
            營收變異係數 = (
                近一年月營收.std() / 近一年月營收.mean()
                if 近一年月營收.mean() > 0
                else 1.0
            )
        else:
            營收變異係數 = 1.0  # 資料不足時預設視為高波動

        # 雙軌精細分流
        if 合約負債佔比 > 0.10 and 營收變異係數 >= 0.15:
            # 專案型、營建型大起大落 (如中菲、互動)
            營收模式 = "大蛇吞象"
            特徵科目佔比 = round(合約負債佔比, 3)
        elif 營收變異係數 < 0.15:
            # 訂閱或常態儲值，月營收極度平滑，但頭頂有蓄水池水庫 (如一零四、數字科技)
            營收模式 = "積雪漸融"
            特徵科目佔比 = round(合約負債佔比, 3)
        else:
            # 其餘合約負債佔比介於 1%~10% 且波動大的公司，先歸入天天進帳中
            營收模式 = "天天進帳"
            特徵科目佔比 = 0.0

    # 3. 軌道 B：期貨經紀金融業
    elif 保證金佔比 > 0.50:
        營收模式 = "食客戶保證金利息"
        特徵科目佔比 = round(保證金佔比, 3)

    # 4. 軌道 C：常態性開門營業公司 (天天進帳型)
    else:
        營收模式 = "天天進帳"
        特徵科目佔比 = 0.0

    return 營收模式, 特徵科目佔比

@通知執行時間
def 分析營收動能(股票) -> "pd.Series":
    """
    一、本函數為最外層「一槍斃命營收動能熔斷器」，採用「無監督特徵分流」機制。
    二、營收模式分為大蛇吞象、積雪漸融、食客戶保證金利息、天天進帳。
    三、大蛇吞象：合約負債占比高(合約負債/總資產 > 10%)且營收波動高(CV >= 0.15)，
        改用「合約負債長線動能」把關。
    四、積雪漸融：若有合約負債，且近年月營收波動低(CV < 0.15)，
        啟動「積雪難繼」硬熔斷與月營收監控。
    五、食客戶保證金利息：若最新一季「期貨交易人權益/總資產 > 50%」，
        改用「客戶保證金長線動能」把關。
    六、天天進帳：其餘常態企業，開啟月營收快速微失速與硬熔斷雷達。
    """
    import numpy as np
    import pandas as pd
    from twse_crawler.營收分析 import 取歷月營收表
    from twse_crawler.財報分析 import 取財報彙總表
    喪失營收動能原因 = None
    r = 分析崑鼎型公司(股票)
    if r.是否為崑鼎型公司:
        r["營收模式"] = '崑鼎型公司'
        r["是否喪失營收動能"]= r.特許經營權是否乾涸
        if r.特許經營權是否乾涸:
            r["喪失營收動能原因"]= "特許經營權乾涸"
        r["喪失營收動能原因"]= None
        return r
    # 1. 讀取最新季報（計算 YoY 需整份表）
    季報表 = 取財報彙總表(股票)

    # 2. 呼叫營收模式鑑定函數
    營收模式, 特徵科目佔比 = 分析營收模式(股票)

    # 初始化輸出指標變數
    最新營收同比 = np.nan
    最新合約負債同比 = 0.0
    最新客戶保證金同比 = 0.0
    是否觸發動能硬熔斷 = False

    # ==========================================
    # 3. 根據營收模式對接對應的動能與熔斷把關機制
    # ==========================================

    # 模式 A：大蛇吞象型 (專案、營建等長線合約控制)
    if 營收模式 == "大蛇吞象":
        去年同季合約負債 = 季報表["合約負債－流動"].shift(4)
        季報表["合約負債同比"] = np.where(
            去年同季合約負債 > 0,
            (季報表["合約負債－流動"] - 去年同季合約負債) / 去年同季合約負債,
            0,
        )
        最新合約負債同比 = 季報表["合約負債同比"].iloc[-1]

        # 熔斷標準：水庫乾涸（按年大減 15% 以上）
        if 最新合約負債同比 < -0.15:
            是否觸發動能硬熔斷 = True

    # 模式 D：積雪漸融型 (一零四、數字科技雙保險雷達)
    elif 營收模式 == "積雪漸融":
        # (1) 計算領先指標：最新季報合約負債同比
        去年同季合約負債 = 季報表["合約負債－流動"].shift(4)
        季報表["合約負債同比"] = np.where(
            去年同季合約負債 > 0,
            (季報表["合約負債－流動"] - 去年同季合約負債) / 去年同季合約負債,
            0,
        )
        最新合約負債同比 = 季報表["合約負債同比"].iloc[-1]

        # (2) 計算同步指標：最新單月營收同比
        月營收表 = 取歷月營收表(股票)
        if not 月營收表.empty:
            去年同月營收 = 月營收表["營收"].shift(12)
            月營收表["月營收同比"] = np.where(
                去年同月營收 > 0,
                (月營收表["營收"] - 去年同月營收) / 去年同月營收,
                0,
            )
            最新營收同比 = 月營收表["月營收同比"].iloc[-1]

        # 雙重把關硬熔斷邏輯：
        # 條件一：山頂無雪可積，蓄水池（合約負債）衰退超過 10%
        合約負債枯竭 = 最新合約負債同比 < -0.10
        # 條件二：月營收出現異常失速 (單月年減超過 15%)
        單月營收失速 = (
            最新營收同比 < -0.15 if not np.isnan(最新營收同比) else False
        )

        if 合約負債枯竭 or 單月營收失速:
            是否觸發動能硬熔斷 = True
            if 合約負債枯竭:
                喪失營收動能原因 = "合約負債同比減少逾1成"
            if 單月營收失速:
                喪失營收動能原因 = "月營收同比減少逾15％"

    # 模式 B：食客戶保證金利息型
    elif 營收模式 == "食客戶保證金利息":
        去年同季保證金 = 季報表["客戶保證金專戶"].shift(4)
        季報表["保證金同比"] = np.where(
            去年同季保證金 > 0,
            (季報表["客戶保證金專戶"] - 去年同季保證金) / 去年同季保證金,
            0,
        )
        最新客戶保證金同比 = 季報表["保證金同比"].iloc[-1]

        if 最新客戶保證金同比 < -0.15:
            是否觸發動能硬熔斷 = True
            喪失營收動能原因 = "客戶保證金減少逾15％"

    # 模式 C：天天進帳型 (常態製造與零售業)
    else:
        月營收表 = 取歷月營收表(股票)
        if 月營收表.empty:
            raise ValueError(f"{股票}無月營收資料！")

        去年同月營收 = 月營收表["營收"].shift(12)
        月營收表["月營收同比"] = np.where(
            去年同月營收 > 0,
            (月營收表["營收"] - 去年同月營收) / 去年同月營收,
            0,
        )
        最新營收同比 = 月營收表["月營收同比"].iloc[-1]

        月營收表["月營收失速"] = 月營收表["月營收同比"] < -0.15
        營收動能連續嚴重失速 = (
            月營收表["月營收失速"].rolling(window=2).sum() == 2
        )
        營收動能連續嚴重失速 = 營收動能連續嚴重失速.iloc[-1]

        if 營收動能連續嚴重失速:
            是否觸發動能硬熔斷 = True
            喪失營收動能原因 = "近二月營收同比連續減少逾15％"

    # ==========================================
    # 4. 打包 Series 輸出
    # ==========================================
    動能評估結果 = pd.Series(
        {
            "營收模式": 營收模式,
            "特徵科目佔比": 特徵科目佔比,
            "最新營收同比": 最新營收同比,
            "最新合約負債同比": 最新合約負債同比,
            "最新客戶保證金同比": 最新客戶保證金同比,
            "是否喪失營收動能": 是否觸發動能硬熔斷,
            "喪失營收動能原因": 喪失營收動能原因
        }
    )
    動能評估結果.name = 股票
    return 動能評估結果
