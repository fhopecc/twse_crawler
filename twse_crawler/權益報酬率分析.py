from zhongwen.程式 import 通知執行時間

@通知執行時間
def 分析股東權益報酬率(股票) -> 'pandas.Series':
    """
    一、分析股東權益報酬率評分20分。
    二、報告項目：評語、分數
    三、評語由高至低分為金蟾、金雞及鷦鷯。
    """
    import pandas as pd
    import numpy as np
    import statsmodels.api as sm
    from twse_crawler.財報分析 import 取財報彙總表

    歷史季度財報 = 取財報彙總表(股票)
    歷史季度財報["股東權益"] = 歷史季度財報.權益總額
    # 1. 建立 TTM 數據
    歷史季度財報["滾動四季淨利"] = 歷史季度財報["淨利"].rolling(window=4).sum()
    歷史季度財報["滾動平均股東權益"] = (
        歷史季度財報["股東權益"] + 歷史季度財報["股東權益"].shift(4)
    ) / 2
    歷史季度財報["滾動四季ROE"] = (
        歷史季度財報["滾動四季淨利"] / 歷史季度財報["滾動平均股東權益"]
    ) * 100

    有效財報資料 = 歷史季度財報.dropna(subset=["滾動四季ROE"]).copy()

    分析數據 = 有效財報資料.tail(40)
    總季度數 = len(分析數據)

    if 總季度數 < 4:
        raise ValueError("資料量不足，至少需要 4 季以上的資料才能計算 TTM 數據。")

    # 2. 計算量化指標
    中位數 = 分析數據["滾動四季ROE"].median()
    平均值 = 分析數據["滾動四季ROE"].mean()
    標準差 = 分析數據["滾動四季ROE"].std(ddof=1)
    變異係數 = 標準差 / 平均值 if 平均值 != 0 else float("inf")
    最低值 = 分析數據["滾動四季ROE"].min()

    最新_TTM = 分析數據["滾動四季ROE"].iloc[-1]
    最新對比值 = 最新_TTM / 中位數 if 中位數 != 0 else 0

    Y = 分析數據["滾動四季ROE"].values
    X = np.arange(1, len(Y) + 1)
    X_加常數項 = sm.add_constant(X)

    迴歸模型 = sm.OLS(Y, X_加常數項).fit()
    趨勢 = 迴歸模型.params[1] if len(迴歸模型.params) > 1 else 0

    # 3. 20 分制評分邏輯
    中位數得分 = 0
    變異係數得分 = 0
    最低值得分 = 0
    最新對比得分 = 0
    趨勢得分 = 0

    if 中位數 >= 20:
        中位數得分 = 6
    elif 中位數 >= 15:
        中位數得分 = 4
    elif 中位數 >= 10:
        中位數得分 = 2

    if 變異係數 <= 0.15:
        變異係數得分 = 6
    elif 變異係數 <= 0.25:
        變異係數得分 = 3

    if 最低值 >= 8:
        最低值得分 = 4

    if 最新對比值 >= 1.0:
        最新對比得分 = 2
    elif 最新對比值 >= 0.9:
        最新對比得分 = 1

    if 趨勢 > 0.05:
        趨勢得分 = 2
    elif -0.05 <= 趨勢 <= 0.05:
        趨勢得分 = 1

    總分 = 中位數得分 + 變異係數得分 + 最低值得分 + 最新對比得分 + 趨勢得分

    if 總分 >= 17:
        評級結果 = "金蟾"
    elif 總分 >= 13:
        評級結果 = "金雞"
    else:
        評級結果 = "鷦鷯"

    # 4. 輸出單層結構報告結果
    分析報告 = pd.Series({
        "評語": f'{評級結果}，依前{總季度數//4}年財報評定',
        "分數": 總分,
        "長期滾動四季權益報酬率中位數": 中位數,
        "長期滾動四季權益報酬率變異係數": 變異係數,
        "長期滾動四季權益報酬率最低值": 最低值,
        "最新滾動四季權益報酬率": 最新_TTM,
        "最新滾動四季與中位數比值": 最新對比值,
        "趨勢": 趨勢,
        "分析數據季度數": 總季度數,
        "中位數得分": 中位數得分,
        "變異係數得分": 變異係數得分,
        "最低值得分": 最低值得分,
        "最新對比得分": 最新對比得分,
        "趨勢得分": 趨勢得分,
    })
    分析報告.name = '股東權益報酬率分析結果'
    return 分析報告

