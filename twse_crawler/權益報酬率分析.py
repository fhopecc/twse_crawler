from zhongwen.程式 import 通知執行時間

@通知執行時間
def 分析股東權益報酬率(股票) -> 'pandas.Series':
    """
    一、分析股東權益報酬率。
    二、分析項目：報酬率長期中位數、報酬率長期變異係數、報酬率長期最低值、最新滾動四季報酬率、
                  報酬率長期斜率、數據季度數
    """
    import pandas as pd
    import numpy as np
    import statsmodels.api as sm
    from twse_crawler.財報分析 import 取財報彙總表

    歷史季度財報 = 取財報彙總表(股票)
    歷史季度財報["股東權益"] = 歷史季度財報.權益總額
    # 1. 建立 TTM 數據
    歷史季度財報["滾動四季淨利"] = 歷史季度財報["淨利"].rolling(window=4).sum()
    歷史季度財報["滾動平均股東權益"] = (
        歷史季度財報["股東權益"] + 歷史季度財報["股東權益"].shift(4)
    ) / 2
    歷史季度財報["滾動四季報酬率"] = (
        歷史季度財報["滾動四季淨利"] / 歷史季度財報["滾動平均股東權益"]
    ) * 100

    有效財報資料 = 歷史季度財報.dropna(subset=["滾動四季報酬率"]).copy()

    分析數據 = 有效財報資料.tail(40)
    總季度數 = len(分析數據)

    if 總季度數 < 4:
        raise ValueError("資料量不足，至少需要 4 季以上的資料才能計算 滾動四季 數據。")

    # 2. 計算量化指標
    中位數 = 分析數據["滾動四季報酬率"].median()
    平均值 = 分析數據["滾動四季報酬率"].mean()
    標準差 = 分析數據["滾動四季報酬率"].std(ddof=1)
    變異係數 = 標準差 / 平均值 if 平均值 != 0 else float("inf")
    最低值 = 分析數據["滾動四季報酬率"].min()

    最新滾動四季報酬率 = 分析數據["滾動四季報酬率"].iloc[-1]

    Y = 分析數據["滾動四季報酬率"].values
    X = np.arange(1, len(Y) + 1)
    X_加常數項 = sm.add_constant(X)

    迴歸模型 = sm.OLS(Y, X_加常數項).fit()
    趨勢 = 迴歸模型.params[1] if len(迴歸模型.params) > 1 else 0
    # 4. 輸出單層結構報告結果
    分析報告 = pd.Series({
        "報酬率長期中位數": 中位數,
        "報酬率長期變異係數": 變異係數,
        "報酬率長期最低值": 最低值,
        "報酬率長期斜率": 趨勢,
        "最新滾動四季報酬率": 最新滾動四季報酬率,
        "分析數據季度數": 總季度數
    })
    分析報告.name = 股票
    return 分析報告

