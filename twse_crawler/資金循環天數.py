def 分析資金循環(股票) -> dict:
    """營運資金循環(CCC)獨立量化函數。

    同時審查 10 年（40季）歷史基準線（常態區間）與近期 4 季滾動趨勢。

    Args:
        歷史季度財報 (pd.DataFrame): 必須包含以下中文欄位：
                                      - '應收帳款'
                                      - '存貨'
                                      - '應付帳款'
                                      - '營業收入' (單季營收)
                                      - '營業成本' (單季成本)

    Returns:
        dict: 包含最新天數、歷史常態基準、短線惡化警訊以及是否觸發紅色熔斷的結果字典。
    """
    import numpy as np
    import pandas as pd
    from twse_crawler.財報分析 import 取財報彙總表
    資料表 = 取財報彙總表(股票)
    資料表['應收帳款'] = 資料表.應收帳款淨額 
    資料表["營業成本"] = 資料表.成本 + 資料表.費用

    # 1. 基礎數據計算 (台灣財報為單季，週轉天數以 90 天計算)
    資料表["應收帳款週轉天數"] = np.where(
        資料表["營收"] > 0, (資料表["應收帳款"] / 資料表["營收"]) * 90, 0
    )
    資料表["存貨週轉天數"] = np.where(
        資料表["營業成本"] > 0, (資料表["存貨"] / 資料表["營業成本"]) * 90, 0
    )
    資料表["應付帳款付現天數"] = np.where(
        資料表["營業成本"] > 0, (資料表["應付帳款"] / 資料表["營業成本"]) * 90, 0
    )

    # 核心指標：營運資金循環天數（CCC）
    資料表["營運資金循環天數_CCC"] = (
        資料表["應收帳款週轉天數"]
        + 資料表["存貨週轉天數"]
        - 資料表["應付帳款付現天數"]
    )

    # 2. 宏觀分析：10 年（40季）歷史基準線 (Baseline)
    # 滾動取過去 40 季的中位數與標準差，定義出這家公司自身的「健康常態範圍」
    長期_CCC_中位數 = 資料表["營運資金循環天數_CCC"].tail(40).median()
    長期_CCC_標準差 = 資料表["營運資金循環天數_CCC"].tail(40).std()

    # 以中位數加上 1.5 倍標準差，作為該公司歷史上的「極限壓力天花板」
    歷史極限天花板 = 長期_CCC_中位數 + (1.5 * 長期_CCC_標準差)

    # 3. 微觀分析：短線滾動趨勢 (近 4 季)
    資料表["當季是否惡化_天數拉長"] = 資料表["營運資金循環天數_CCC"].diff() > 0
    # 檢查是否連續 4 個季度，CCC 天數都比上一季還要拉長
    是否連續4季惡化 = 資料表["當季是否惡化_天數拉長"].rolling(window=4).sum() == 4

    # 4. 提取最新一季的審查數據
    最新一季數據 = 資料表.iloc[-1]
    最新_CCC_天數 = 最新一季數據["營運資金循環天數_CCC"]

    # 5. 終極熔斷判定：如果近期連續惡化，且最新天數已經「衝破 10 年歷史天花板」
    是否衝破歷史極限 = 最新_CCC_天數 > 歷史極限天花板
    觸發紅色熔斷 = 是否連續4季惡化.iloc[-1] and 是否衝破歷史極限

    # 打包成標準字典輸出，供主控台進行最終權重或乘數綜合打分
    評估結果 = {
        "指標名稱": "營運資金循環模型 (CCC)",
        "最新天數": round(最新_CCC_天數, 1),
        "十年中位數基準": round(長期_CCC_中位數, 1),
        "歷史天花板臨界點": round(歷史極限天花板, 1),
        "近期是否連續4季惡化": 是否連續4季惡化.iloc[-1],
        "是否衝破歷史極限": 是否衝破歷史極限,
        "觸發紅色熔斷": 觸發紅色熔斷,  # 核心邏輯閘：True 代表必須啟動「倍數懲罰」
    }

    return 評估結果
