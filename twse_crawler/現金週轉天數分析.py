def 分析現金週轉天數(股票) -> "pandas.Series":
    """
    一、現金週轉天數係報酬率之原因及領先指標，天數增加即經營效率惡化，
        將致未來報酬率降低。
    二、報告項目：最新週轉天數、週轉天數長期中位數、可接受週轉天數天花板、
                  週轉天數近四季是否連續惡化、近期惡化是否逾天花板、
                  最新應付帳款天數是否增加、應付帳款信用惡化、
                  現金週轉是否嚴重惡化
    """
    import numpy as np
    import pandas as pd
    from twse_crawler.財報分析 import 取財報彙總表

    h = 取財報彙總表(股票)
    m = []
    h['應收帳款'] = h.應收帳款淨額.fillna(0) + h.應收票據淨額.fillna(0) 
    h['應付票據及帳款'] = h.應付票據.fillna(0) + h.應付帳款.fillna(0) 
    h["營業成本"] = h.成本.fillna(0) + h.費用.fillna(0) \
                  - h.折舊費用.fillna(0) - h.攤銷費用.fillna(0)
    h["營業現金流"]  = h['營業活動之淨現金流入（流出）'].fillna(0)

    # ==========================================
    # 1. 基礎數據計算 (台灣財報為單季，週轉天數以 90 天計算)
    # ==========================================
    h["應收帳款收現天數"] = np.where(
        h.營收 > 0, (h.應收帳款 / h.營收) * 90, 0
    )
    h['成本占比'] = h.成本.fillna(0)/h.營收.fillna(0)
    h["存貨週轉天數"] = np.where(
        h.成本占比 > 0.1, (h.存貨.fillna(0) / h.成本.fillna(0)) * 90, 0
    )
    h["應付帳款付現天數"] = np.where(
        h.營業成本 > 0, (h.應付帳款 / h.營業成本) * 90, 0
    )

    # 核心指標：營運現金週轉天數（CCC）
    h["現金週轉天數"] = (
        h["應收帳款收現天數"]
        + h["存貨週轉天數"]
        - h["應付帳款付現天數"]
    )

    # ==========================================
    # 2. 宏觀分析：10 年（40季）歷史基準線 (Baseline)
    # ==========================================
    # 滾動取過去 40 季的中位數與標準差，定義出這家公司自身的「健康常態範圍」
    長期中位數 = h["現金週轉天數"].tail(40).median()
    長期標準差 = h["現金週轉天數"].tail(40).std()

    # 以中位數加上 1.5 倍標準差，作為該公司歷史可接受週轉天數天花板 
    週轉天數天花板 = 長期中位數 + (1.5 * 長期標準差)

    # ==========================================
    # 3. 微觀分析：短線滾動趨勢 (近 4 季與聯立雷達)
    # ==========================================
    # (1) 現金週轉天數滾動檢查
    h["當季是否惡化"] = h["現金週轉天數"].diff() > 0
    連續四季惡化 = h["當季是否惡化"].rolling(window=4).sum() == 4

    # (2) 🛡️ 應付帳款惡性拖欠雷達聯立檢查
    # 檢查各項指標是否比上一季增加
    h["應付帳款天數增加"] = h["應付帳款付現天數"].diff() > 0
    h["存貨天數增加"] = h["存貨週轉天數"].diff() > 0
    # 聯立條件：1. 應付帳款天數增加 且 2. 營業現金流為負(失血) 且 3. 存貨天數增加(滯銷)
    # (備註：此處假設您的財報彙總表內建有 '營業現金流' 欄位)
    h["應付帳款信用惡化"] = (
        h["應付帳款天數增加"] & 
        (h["營業現金流"] < 0) & 
        h["存貨天數增加"]
    )

    # ==========================================
    # 4. 提取最新一季的審查數據
    # ==========================================
    最新一季數據 = h.iloc[-1]
    最新週轉天數 = 最新一季數據["現金週轉天數"]
    最新應付帳款天數增加 = 最新一季數據["應付帳款天數增加"]
    最新應付帳款信用惡化 = 最新一季數據["應付帳款信用惡化"]

    # ==========================================
    # 5. 終極熔斷判定
    # ==========================================
    近期惡化逾天花板 = 最新週轉天數 > 週轉天數天花板

    # ==========================================
    # 打包成標準字典輸出，供主控台進行最終綜合打分
    # ==========================================
    評估結果 = pd.Series({
        "最新週轉天數": round(最新週轉天數, 1),
        "最新應付帳款付現天數": round(最新一季數據.應付帳款付現天數, 1),
        "最新成本占比": round(最新一季數據.成本占比, 1),
        "最新存貨週轉天數": round(最新一季數據.存貨週轉天數, 1),
        "最新應收帳款收現天數": round(最新一季數據.應收帳款收現天數, 1),
        "週轉天數長期中位數": round(長期中位數, 1),
        "週轉天數天花板": round(週轉天數天花板, 1),
        "週轉天數近四季是否連續惡化": 連續四季惡化.iloc[-1],
        "近期惡化是否逾天花板": 近期惡化逾天花板,
        "最新應付帳款天數是否增加": 最新應付帳款天數增加,
        "最新應付帳款信用是否惡化": 最新應付帳款信用惡化,
        "評語": '；'.join(m)
    })
    評估結果.name = 股票
    return 評估結果

def 評定現金週轉天數分數(股票) -> "pandas.Series":
    '''
    一、第二部分：現金週轉天數配分 10 分。
    '''
    # ==========================================
    # 
    # ==========================================
    現金週轉天數分析結果 = r = 分析現金週轉天數(股票)
    最新週轉天數 = 現金週轉天數分析結果["最新週轉天數"]
    週轉天數長期中位數 = 現金週轉天數分析結果["週轉天數長期中位數"]
    週轉天數天花板 = 現金週轉天數分析結果["週轉天數天花板"]
    週轉天數近四季連續惡化 = 現金週轉天數分析結果["週轉天數近四季是否連續惡化"]
    應付天數增加 = 現金週轉天數分析結果["最新應付帳款天數是否增加"]
    信用惡化 = 現金週轉天數分析結果["最新應付帳款信用是否惡化"]
    近期惡化逾天花板 = 現金週轉天數分析結果["近期惡化是否逾天花板"]
    週轉天數級分 = 0
    週轉天數調整分 = 0
    m = r.評語 if len(r.評語) > 0 else []
    if 週轉天數長期中位數 <= 0:
        週轉天數級分 = 10
        m.append(f'用別人的錢賺錢，其長期週轉天數穩定為負數({週轉天數長期中位數:,.0f})')
    elif 週轉天數長期中位數 <= 30:
        週轉天數級分 = 8
    elif 週轉天數長期中位數 <= 60:
        週轉天數級分 = 6
    elif 週轉天數長期中位數 <= 90:
        週轉天數級分 = 3
    else:
        週轉天數級分 = 0

    if 週轉天數近四季連續惡化:
        週轉天數調整分 -= 1
    elif not 週轉天數近四季連續惡化 and 最新週轉天數 < 週轉天數長期中位數:
        週轉天數調整分 += 1

    if 信用惡化:
        週轉天數調整分 -= 1.0
    elif not 應付天數增加 and 最新週轉天數 < 週轉天數天花板:
        週轉天數調整分 += 1.0

    if (週轉天數近四季連續惡化 and 近期惡化逾天花板) or 信用惡化:
        週轉天數級分 = 0
        週轉天數調整分 = 0

    if 週轉天數近四季連續惡化 and 近期惡化逾天花板:
        m.append("🚨【注意：週轉天數近四季連續惡化且已逾歷史天花板{週轉天數天花板:.,0f}天】")
    if 信用惡化:
        m.append("🚨【營運資金不足警訊：應付帳款天數增加，惟營業現金流為負(失血)且存貨天數增加(滯銷)】")

    最終現金週轉天數得分 = max(0, min(10, 週轉天數級分 + 週轉天數調整分))
    m = [f"【現金週轉天數】得 {最終現金週轉天數得分:,.0f} 分"] + m
    r['分數'] = 最終現金週轉天數得分
    r['評語'] = '；'.join(m)
    return r
