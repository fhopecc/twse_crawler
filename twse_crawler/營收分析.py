def 分析月營收(股票, 重新分析=False):
    '''
    一、結果項目：營收增減次數、趨勢斜率、分數(5%)、評語。
    二、分數空值者以零填補。
    三、短期及長期營收分別係近一年及近一季期間平均營收；
        據薛兆亨教授podcast指出，景氣循環周期一般為7年，
        一半期間趨勢向上，另一半向下，故趨勢分析資料期間為3年半(42個月)，
        「同比連續消長次數」、「短期營收較長期增減率」等指標。
    '''
    from twse_crawler.自結損益 import 預測前年至次年周期數據
    from 股票分析.趨勢分析 import 分析歷月數據增減情形, 年化趨勢評分
    from twse_crawler.股票基本資料分析 import 查股票簡稱
    from zhongwen.時 import 今日
    from zhongwen.表 import 顯示, 數據不足
    import pandas as pd
    import numpy as np
    歷月營收 = 取歷月營收表(股票)
    股票簡稱 = 查股票簡稱(股票)
    try:
        最近月營收 = 歷月營收.iloc[-1]
    except IndexError:
        raise 數據不足(f'{股票簡稱}月營收', 0, 1, '分析月營收')
    公司名稱 = 最近月營收.公司名稱
    最近營收月份 = 最近月營收.營收月份
    logger.info(f'分析{公司名稱}截至{最近營收月份.month}月歷月營收……')
    增減情形 = 分析歷月數據增減情形(歷月營收
                                   ,'營業收入-當月營收'
                                   ,'營收月份'
                                   ,'營業收入-去年同月增減(%)'
                                   ,'營業收入-上月比較增減(%)'
                                   ,'備註'
                                   ,'營收'
                                   )
    預測結果 = 預測前年至次年周期數據(歷月營收
                                     ,'營業收入-當月營收', '營收月份'
                                     ,數據名稱='營收'
                                     )
    分數 = 0
    if not 增減情形.empty:
        if 增減情形.同比 > 0:
            分數 = min(( 增減情形.同比*abs(增減情形.連續增減月數) ) / 0.1, 5)
    評語 = 增減情形.說明
    if len(預測結果.趨勢說明) > 0:
        評語 = 增減情形.說明 + "，" + 預測結果.趨勢說明.replace('營收', '')
    分析結果 = pd.Series()
    分析結果['股票'] = 股票
    分析結果['趨勢斜率'] = 預測結果.趨勢斜率
    分析結果['連續增減月數'] = 增減情形.連續增減月數
    分析結果['分數'] = int(分數) if not pd.isna(分數) else 0
    分析結果['評語'] = 評語 + f'{分析結果.分數:.0f}'
    分析結果['營收月份'] = 最近營收月份
    return 分析結果

