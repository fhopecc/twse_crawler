def 依台積電資本支出預測營收(股票):
    '''
    一、依股票發行公司歷月營收預測截至公布月營收當季之各季營收。
    二、次依台積電資本支出預測公布月營收次季至次年各季營收
    三、預測結果：前年至次年各季營收、預測說明、營收趨勢起日、營收數據頻率、
        營收趨勢方向、營收趨勢斜率、營收趨勢說明
    三、未公布月營收者，產生「數據不足」例外。
    四、僅公布1個月營收者，以該月營收數設算前年至次年各月營收。
    '''
    from twse_crawler.股票基本資料分析 import 查股票簡稱, 查股票代號
    from zhongwen.時 import 上年數, 今年數, 取民國年度, 取民國月份, 取季別
    from twse_crawler.自結損益 import 預測前年至次年周期數據
    from 股票分析.財報分析 import 取歷年營收占台積電資本資出比例
    from zhongwen.快取 import 刪除指定名稱快取
    from zhongwen.表 import 顯示, 數據不足
    import pandas as pd
    股票 = 查股票代號(股票)
    股票簡稱 = 查股票簡稱(股票)
    
    h = 歷月營收表 = 取歷月營收表(股票)

    公司代號 = h.iloc[-1].公司代號
 
    c = 歷月營收表.iloc[-1]

    公司代號, 公司簡稱 = 股票, 股票簡稱

    歷月營收表['營收日期'] = 歷月營收表.營收月份.map(lambda p:p.end_time.normalize())
    try:
        營收預測結果 = 前年至次年各月營收, 預測說明, _, _, _, 趨勢說明, *_ = \
            預測前年至次年周期數據(歷月營收表
                ,'營業收入-當月營收', '營收月份'
                ,數據名稱='營收'
                ,顯示趨勢圖=False
                )
    except 數據不足 as e:
        e.名稱 = f'{股票簡稱}營收表'
        e.目的 = '預測前年至次年營收'
        raise e 
    歷月營收 = 歷月營收表[['營業收入-當月營收', '營收日期']].set_index('營收日期')
    歷月營收 = 歷月營收['營業收入-當月營收']
    歷月營收 = 歷月營收.combine_first(前年至次年各月營收)
    歷季營收 = 歷月營收.resample('QE').sum()  
    公布月當季 = 取季別(歷月營收表.iloc[-1].營收月份)
    截至公布月營收當季之各季營收 = 歷季營收[歷季營收.index < (公布月當季+1).start_time.normalize()]
    截至公布月營收當季之各季營收 = 截至公布月營收當季之各季營收.to_frame().reset_index()
    df = 截至公布月營收當季之各季營收 = 截至公布月營收當季之各季營收.rename(
            columns={'index':'營收日期'})
    df = df.set_index('營收日期')
    台積電資本支出預測結果 = 依台積電月營收預測次年資本支出()
    s = 截至次年各季台積電資本支出 = 台積電資本支出預測結果.截至次年各季台積電資本支出
    df = df.reindex(df.index.union(s.index))
    df['截至次年各季台積電資本支出'] = 截至次年各季台積電資本支出
    
    first_valid = df.截至次年各季台積電資本支出.first_valid_index()
    # 保留從第一個非空值開始的資料
    df = df.loc[first_valid:]
    df['營收日期'] = df.index
    # 顯示(df.dropna(subset=['營業收入-當月營收']), 顯示索引=True)

